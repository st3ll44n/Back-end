            <ul>
                <li>500g de peito de frango cortado em cubos</li>
                <li>1 cebola média picada</li>
                <li>2 dentes de alho picados</li>
                <li>1 colher de sopa de gengibre ralado</li>
                <li>2 colheres de sopa de curry em pó</li>
                <li>1 colher de chá de sal (ou a gosto)</li>
                <li>1 pitada de pimenta-do-reino</li>
                <li>1 colher de sopa de óleo ou azeite</li>
                <li>1 vidro (200ml) de leite de coco</li>
                <li>Salsinha ou coentro picado (opcional para finalizar)</li>
                <li>Arroz branco para acompanhar</li>
            </ul>
        </div>

        <!-- Modo de Preparo -->
        <div class="section-box">
            <h4>Modo de Preparo</h4>
            <ol>
                <li>Em uma panela média, aqueça o óleo e refogue a cebola até ficar transparente.</li>
                <li>Adicione o alho e o gengibre e refogue por mais 1 minuto, mexendo bem.</li>
                <li>Acrescente os cubos de frango e doure por todos os lados.</li>
                <li>Tempere com o curry, sal e pimenta. Misture bem para envolver todo o frango nos temperos.</li>
                <li>Despeje o leite de coco na panela, abaixe o fogo e cozinhe por cerca de 20 minutos ou até o frango estar completamente cozido e o molho mais encorpado.</li>
                <li>Se desejar, finalize com salsinha ou coentro picado por cima.</li>
                <li>Sirva quente acompanhado de arroz branco.</li>
            </ol>
        </div>

