            <h4>Modo de Preparo</h4>
            <ol>
                <li>No liquidificador, bata as cenouras, ovos e óleo até obter um creme homogêneo.</li>
                <li>Em uma tigela, misture o creme com o açúcar, a farinha e o fermento.</li>
                <li>Despeje a massa em uma forma untada e enfarinhada.</li>
                <li>Asse em forno pré-aquecido a 180ºC por cerca de 40 minutos.</li>
                <li>Para a cobertura, leve todos os ingredientes ao fogo baixo até ferver e engrossar um pouco.</li>
                <li>Despeje sobre o bolo ainda quente.</li>
            </ol>
